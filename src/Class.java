public class Class {
    private int number;
    private String soz;
    private int[] mas;




    public Class(int number, String soz, int[] mas) {
        this.number = number;
        this.soz = soz;
        this.mas = mas;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getSoz() {
        return soz;
    }

    public void setSoz(String soz) {
        this.soz = soz;
    }

    public int[] getMas() {
        return mas;
    }

    public void setMas(int[] mas) {
        this.mas = mas;
    }
}

